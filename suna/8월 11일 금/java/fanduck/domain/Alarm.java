package fanduck.domain;


public class Alarm{
  int alarmNo;
  int mno;
  boolean read;
  String mvRegisterDate;
  
public int getAlarmNo() {
	return alarmNo;
}
public void setAlarmNo(int alarmNo) {
	this.alarmNo = alarmNo;
}
public int getMno() {
	return mno;
}
public void setMno(int mno) {
	this.mno = mno;
}
public boolean isRead() {
	return read;
}
public void setRead(boolean read) {
	this.read = read;
}
public String getMvRegisterDate() {
	return mvRegisterDate;
}
public void setMvRegisterDate(String mvRegisterDate) {
	this.mvRegisterDate = mvRegisterDate;
}
 
  
}